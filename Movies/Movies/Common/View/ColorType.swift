import UIKit

enum ColorType: String {
	
	/// #000000 = rgb(0, 0, 0)
	case black = "#000000"
	
	/// #B8E5FF = rgb(184, 229, 255)
	case regular = "#B8E5FF"
	
	/// 001B43 = rgb(0, 27, 67)
	case darkBlue = "#001B43"
}
